#Exponentes -> **
print(2**3)
print(6**2)

#Multiplicacion -> *
print(2*3)
print(6*2)

#Division -> /
print(2/3)
print(6/2)

#Division entera -> // (El resultado siempre va a ser entero, se redondea hacia abajo)
print(2//3)
print(6//2)

#Modulo -> %
print(14%4)
print(10%5)

#Suma -> +
print(2+4)

#Resta -> -
print(5-2)
print(-6-10)

#Concatenacion -> string + string
x = "Hola"
y = "Mundo"
print(x + " " + y)

#Replicacion -> string * number
z = "Mariana"
print(z*3)
print(z*-1)#Un number menor o igual a cero produce una cadena vacia

#Operador de igualdad ==
print(2==2) #True
print(2==2.0) #True
print(1==2) #True

#Desigualdad !=
print(1 != 2) #True
print(1 != 1) #False
print(1 != 1.0) #False

#Mayor que > y mayor o igual que >=
print(2 > 1) #True
print(2 >= 2) #True
print(1 > 2) #False

#Menor que < y menor o igual que <=
print(2 < 1) #False
print(2 <= 2) #True
print(1 < 2) #True


